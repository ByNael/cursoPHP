<?php

/*
 * Aula sobre operadores de atribuição
 *
 * Explicação sobre como utilizar operadores aritméticos
 * e melhorar a codificação de expressões matemáticas com PHP.
 *
 * Operadores:
 * += -= /= *= %=
 */

$itemA = 50;
$itemA += 25;

echo $itemA . PHP_EOL;

$itemA -= 25;

echo $itemA . PHP_EOL;

$itemA /= 2;

echo $itemA . PHP_EOL;

$itemA *= 3;

echo $itemA . PHP_EOL;

$itemA %= 3;

echo $itemA . PHP_EOL;